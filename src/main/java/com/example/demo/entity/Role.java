package com.example.demo.entity;

/**
 * Перечисление (Enum) для определения всех ролей пользователей в системе.
 * Использование Enum повышает надежность и читаемость кода.
 */
public enum Role {

    /**
     * Администратор системы, имеет полный доступ ко всем CRUD-операциям.
     */
    ADMIN,

    /**
     * Обычный клиент, может просматривать расписание и бронировать занятия.
     */
    CLIENT,

    /**
     * Тренер/инструктор, видит расписание и списки клиентов только для своих занятий.
     */
    TRAINER;

    /**
     * Метод возвращает строковое представление роли, которое используется в Spring Security (начинается с "ROLE_").
     * Например: Role.ADMIN.getSpringRole() вернет "ROLE_ADMIN".
     */
    public String getSpringRole() {
        return "ROLE_" + this.name();
    }
}